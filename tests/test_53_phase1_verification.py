class TestPhase1Integration:
    """End-to-end: projection → gateway → skill cleanup."""

    def test_project_then_read_memory_from_gateway(self, tmp_path: Path):
        """Write via gateway, verify projection files reflect it."""
        from core.memory.memory_write_gateway import (
            MemoryWriteGateway,
            WriteContext,
        )
        import asyncio

        service = _make_memory_service(tmp_path)
        settings = _make_settings(tmp_path)
        gateway = MemoryWriteGateway(service, settings)

        patch = MemoryPatch(items=[
            MemoryPatchItem(
                memory_type=MemoryType.PROJECT_MEMORY,
                action=MemoryPatchAction.ADD,
                content="Integration test project memory",
                confidence=0.9,
                importance=0.8,
                scope="integration",
            )
        ])
        context = WriteContext(source="ai_patch", triggered_by="test")

        async def run():
            return await gateway.submit_patch(patch, context)

        result = asyncio.run(run())
        assert len(result.accepted) == 1

        # Verify projection files exist and contain the content
        project_md = tmp_path / "data" / "memory" / "PROJECT.md"
        memory_md = tmp_path / "workspace" / "memory" / "MEMORY.md"

        assert project_md.exists(), "PROJECT.md should exist"
        assert memory_md.exists(), "MEMORY.md should exist"

        pm = project_md.read_text(encoding="utf-8")
        mm = memory_md.read_text(encoding="utf-8")
        assert "Integration test project memory" in pm
        assert "Integration test project memory" in mm

    def test_skill_disable_prevents_nanobot_discovery(self, tmp_path: Path):
        """After disable, skill file should NOT exist in workspace/skills/."""
        from core.skills.skill_schema import SkillRecord

        mgr = _make_skill_manager(tmp_path)
        record = SkillRecord(
            id=str(uuid.uuid4()),
            name="test-nanobot-skill",
            path="",
            description="Should be removed",
        )
        created = mgr.create_skill(record, "# Test\n\nContent")
        assert Path(created.path).exists()

        mgr.disable_skill(created.id)

        # Verify the skill file is gone
        assert not Path(created.path).exists()
        # Verify parent directory is cleaned up
        assert not Path(created.path).parent.exists()
