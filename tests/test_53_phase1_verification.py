class TestSkillFileCleanup:
    """Verify disabled/archived skills have their workspace files removed."""

    def test_disable_skill_removes_file(self, tmp_path: Path):
        from core.skills.skill_schema import SkillRecord, SkillStatus

        mgr = _make_skill_manager(tmp_path)
        record = SkillRecord(
            id=str(uuid.uuid4()),
            name="test-skill",
            path="",
            description="Test skill",
        )
        created = mgr.create_skill(record, "# Test Skill\n\nTest content")
        assert Path(created.path).exists(), "Skill file should exist after creation"

        mgr.disable_skill(created.id)
        assert not Path(created.path).exists(), (
            "Skill file should be removed after disable"
        )

    def test_archive_skill_removes_original_file(self, tmp_path: Path):
        from core.skills.skill_schema import SkillRecord, SkillStatus

        mgr = _make_skill_manager(tmp_path)
        record = SkillRecord(
            id=str(uuid.uuid4()),
            name="archive-test",
            path="",
            description="Archive test skill",
        )
        created = mgr.create_skill(record, "# Archive Skill\n\nContent")
        original_path = Path(created.path)
        assert original_path.exists()

        mgr.archive_skill(created.id)
        assert not original_path.exists(), (
            "Original skill file should be removed after archive"
        )
        # Archive copy should exist
        archive_file = mgr._archive_dir / f"{created.name}_v{created.version}.md"
        assert archive_file.exists(), "Archive copy should exist"

    def test_disable_skill_cleans_empty_dir(self, tmp_path: Path):
        from core.skills.skill_schema import SkillRecord

        mgr = _make_skill_manager(tmp_path)
        record = SkillRecord(
            id=str(uuid.uuid4()),
            name="cleanup-test",
            path="",
            description="Cleanup test",
        )
        created = mgr.create_skill(record, "# Cleanup\n\nTest")
        skill_dir = Path(created.path).parent
        assert skill_dir.exists()

        mgr.disable_skill(created.id)
        assert not skill_dir.exists(), (
            "Empty skill directory should be removed after disable"
        )

    def test_active_skill_file_preserved(self, tmp_path: Path):
        from core.skills.skill_schema import SkillRecord, SkillStatus

        mgr = _make_skill_manager(tmp_path)
        record = SkillRecord(
            id=str(uuid.uuid4()),
            name="active-skill",
            path="",
            description="Active skill",
            status=SkillStatus.ACTIVE,
        )
        created = mgr.create_skill(record, "# Active\n\nContent")
        assert Path(created.path).exists(), "Active skill file should exist"
        # Check DB status is active
        loaded = mgr.get_skill(created.id)
        assert loaded is not None
        assert loaded.status == SkillStatus.ACTIVE


